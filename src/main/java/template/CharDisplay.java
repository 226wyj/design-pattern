package template;

public class CharDisplay extends AbstractDisplay {
    final private char ch;

    public CharDisplay(final char ch) {
        this.ch = ch;
    }

    @Override
    public void open() {
        System.out.print("<<");
    }

    @Override
    public void print() {
        System.out.print(ch);
    }

    @Override
    public void close() {
        System.out.println(">>");
    }
}
